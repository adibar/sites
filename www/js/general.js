function remove_ids(lobj) {
  for (var key in lobj.pages) {
    if (lobj.pages.hasOwnProperty(key)) {
      for (var i=0 ; i<lobj.pages[key].length ; i++) {
        delete lobj.pages[key][i]['id'];
      }      
    }
  }
}



